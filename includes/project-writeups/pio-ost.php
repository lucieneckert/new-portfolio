<p>This is the soundtrack I designed for the <a href='http://www.cs.cornell.edu/courses/cs3152/2021sp/'>INFO
        3152: Introduction to Computer Game Design</a>
    course at Cornell. The central feature of the soundtrack was the ability to switch between "stealth" and
    "chase" themes seamlessly when a player is either evading detection or being spotted or chased. The
    soundtrack was produced with LMMS, using recorded samples and the built-in zynaddsubfx plugin.
</p>
